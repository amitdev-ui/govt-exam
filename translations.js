// Function to get translation
function getTranslation(key, language = 'en') {
    const keys = key.split('.');
    let value = translations;
    
    for (const k of keys) {
        if (value[k] === undefined) {
            console.warn(`Translation key not found: ${key}`);
            return key;
        }
        value = value[k];
    }
    
    return value[language] || value.en;
}

// Function to translate all elements with data-translate attribute
function translatePage(language = 'en') {
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.textContent = getTranslation(key, language);
    });
}

// Initialize translations
document.addEventListener('DOMContentLoaded', function() {
    const savedLanguage = localStorage.getItem('preferredLanguage') || 'en';
    translatePage(savedLanguage);
});

// Universal translation function to be used with the Hindi translation button
document.addEventListener('DOMContentLoaded', function() {
    // Get translation buttons from navbar
    const translateBtn = document.getElementById('translateBtn');
    const mobileTranslateBtn = document.getElementById('mobileTranslateBtn');
    
    if (translateBtn && mobileTranslateBtn) {
        let isHindi = false;
        
        // Function to toggle languages
        const toggleLanguage = () => {
            isHindi = !isHindi;
            translatePage(isHindi ? 'hi' : 'en');
            
            // Update button text
            translateBtn.textContent = isHindi ? 'View in English' : 'हिंदी में देखें';
            mobileTranslateBtn.textContent = isHindi ? 'View in English' : 'हिंदी में देखें';
            
            // Store language preference
            localStorage.setItem('language', isHindi ? 'hi' : 'en');
        };
        
        // Add event listeners
        translateBtn.addEventListener('click', toggleLanguage);
        mobileTranslateBtn.addEventListener('click', toggleLanguage);
        
        // Check for stored language preference
        const storedLanguage = localStorage.getItem('language');
        if (storedLanguage === 'hi') {
            isHindi = false; // Will be toggled to true
            toggleLanguage();
        }
    }
}); 